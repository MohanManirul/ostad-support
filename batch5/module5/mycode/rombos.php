<?php
$rows = 5; // রম্বসের জন্য কতো রো (সারি) হবে সেটি নির্ধারণ করা হয়েছে

// রম্বসের উপরের অংশ প্রিন্ট করার জন্য লুপ
for ($i = 1; $i <= $rows; $i++) {
    // স্পেস প্রিন্ট করা
    for ($j = $i; $j < $rows; $j++) {
        echo "&nbsp;"; // HTML স্পেসের জন্য &nbsp; ব্যবহার করা হয়েছে
    }
    
    // স্টার (*) প্রিন্ট করা
    for ($k = 1; $k <= (2 * $i - 1); $k++) {
        echo "*";
    }
    
    echo "<br>"; // পরের লাইনে যেতে হবে
}

// রম্বসের নিচের অংশ প্রিন্ট করার জন্য লুপ
for ($i = $rows - 1; $i >= 1; $i--) {
    // স্পেস প্রিন্ট করা
    for ($j = $rows; $j > $i; $j--) {
        echo "&nbsp;";
    }
    
    // স্টার (*) প্রিন্ট করা
    for ($k = 1; $k <= (2 * $i - 1); $k++) {
        echo "*";
    }
    
    echo "<br>"; // পরের লাইনে যেতে হবে
}
?>
